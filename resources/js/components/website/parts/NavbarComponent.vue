<script>
export default {
    data: () => ({
        recipients: [],
        occasions: [],
        categories: [],
        currencies: [],
        cities: [],
        user: "",
        cityName: "",
    }),

    computed: {
        chunkedCities() {
            return _.chunk(this.cities, 13);
        },
        currencyActive() {
            return this.$store.getters.selectedCurrency;
        },
        locationActive() {
            return this.$store.getters.selectedLocation;
        },
    },

    methods: {
        saveCurrency(currency) {
            this.$store.dispatch("saveCurrency", currency);
        },
        saveLocation(city) {
            this.$store.dispatch("saveLocation", city);
            $("#dropdownLocation").dropdown("hide");
        },
        displayMenu() {
            let sidenav = document.querySelector(".dashboard-side-nav");
            sidenav.classList.toggle("show-nav");
        },

        preventDropdownClose(e) {
            e.stopPropagation();
        },

        loadData() {
            axios
                .get("/api/navdata")
                .then((response) => {
                    this.recipients = response.data.recipients;
                    this.occasions = response.data.occasions;
                    this.categories = response.data.categories;
                    this.currencies = response.data.currencies;
                    this.user = response.data.user;
                })
                .catch((error) => console.log(error));
        },

        loadCities() {
            if (this.cities == "") {
                axios
                    .get("/api/locations/cities")
                    .then(({ data }) => {
                        this.cities = data;
                    })
                    .catch((error) => console.log(error));
            }
        },
    },
    mounted() {
        this.loadData();
        Fire.$on("reloadRecords", () => {
            this.loadData();
        });
    },
};
</script>
